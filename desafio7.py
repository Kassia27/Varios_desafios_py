# Faça um algoritmo que leia dois valores booleanos (lógicos) e determine se ambos são VERDADEIRO ou FALSO.

opcao1 = bool(True)
opcao2 = bool(False)

print(f"{opcao1} e igual a  {opcao2} : ", opcao1 == opcao2)
print(f"{opcao1} e maior que {opcao2} : ", opcao1 > opcao2)
print(f"{opcao1} e menor que {opcao2} : ", opcao1 < opcao2)
print(f"{opcao1} e diferente a  {opcao2} : ", opcao1 != opcao2)


   